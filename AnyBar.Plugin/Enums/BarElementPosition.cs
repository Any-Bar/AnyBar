namespace AnyBar.Plugin;

/// <summary>
/// Enumeration for the position of bar elements.
/// </summary>
public enum BarElementPosition
{
    /// <summary>
    /// Left position of a hozitontal appbar.
    /// </summary>
    Left,

    /// <summary>
    /// Top position of a vertical appbar.
    /// </summary>
    Top,

    /// <summary>
    /// Center position of a horizontal appbar.
    /// </summary>
    HorizontalCenter,

    /// <summary>
    /// Center position of a vertical appbar.
    /// </summary>
    VerticalCenter,

    /// <summary>
    /// Right position of a horizontal appbar.
    /// </summary>
    Right,

    /// <summary>
    /// Bottom position of a vertical appbar.
    /// </summary>
    Bottom
}
